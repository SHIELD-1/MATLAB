%原始数据
X=[0 0.02 0.04 0.06 0.08 0.10];
Y=[-2.73 0.56 3.96 7.40 10.88 14.42];
n=6; %一共5个变量
 
x2=sum(X.^2); %?求Σ(xi^2)
x1=sum(X); %?求Σ(xi)
x1y1=sum(X.*Y); %?求Σ(xi*yi)
y1=sum(Y); %?求Σ(yi)
 
A=(y1*x2-x1y1*x1)/(n*x2-x1*x1); %解出直线斜率
B=(n*x1y1-x1*y1)/(n*x2-x1*x1); %解出直线截距
%作图
%先把原始数据点用蓝色十字描出来
figure 
plot(X,Y,'+'); 
hold on
%?用红色绘制拟合出的直线
px=0:0.01:0.1;%这里直线区间根据自己实际需求改写
py=A+B*px; 
plot(px,py,'r'); 
